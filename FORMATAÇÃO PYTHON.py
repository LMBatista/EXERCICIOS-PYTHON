hora=10
minutos=20
segundos=50
# Saída de dados com separador
print(str(hora)+':'+str(minutos)+':'+str(segundos))

#Saída de dados com f()
print(f'{hora}:{minutos}:{segundos}')

#Saída de dados com format()
print('{}:{}:{}'.format(hora,segundos,minutos))

#Impresão de Substrings
Imagem=1,2,3,4,5,6,7
print(Imagem[3:6])

#Impresão de Substrings inversamente
Imagem=1,2,3,4,5,6,7
print(Imagem[::-2])

